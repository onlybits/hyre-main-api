namespace Hyre.Modules.Jobs.Core.Enums;

/// <summary>
///   Represents the experience level of a candidate.
/// </summary>
public enum ExperienceLevel
{
	/// <summary>
	///   When you are starting your career and have no experience.
	/// </summary>
	Entry,

	/// <summary>
	///   When you have beginner experience in the field.
	/// </summary>
	Junior,

	/// <summary>
	///   When you have some experience in the field.
	/// </summary>
	Mid,

	/// <summary>
	///   When you have a lot of experience in the field.
	/// </summary>
	Senior,

	/// <summary>
	///   When you have a lot of experience in the field and are a specialist.
	/// </summary>
	Specialist
}